# Definition and Usage of Python print() function:
# The print() function prints the specified message to the screen, 
# or other standard output device. In the four examples applying
# the print() function for view our result.
# ---------------------------------------------------------------

# Now review the four form of execute our first python program of "hello world"

# In the first form, we do use a string
print("hello world!")

# In the second form, we do use a variable assigning a string:
vstr = "hello world!"
print(vstr)

# In the third form, we do use a variable assigning a string and string concatenate:
print(vstr + " with python!")

# In the four form, we do use a variable assigning a string, string concatenate and specify the separator:
print(vstr, " with python!", sep="--")